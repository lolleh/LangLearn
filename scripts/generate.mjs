function makeWordFile(lang, prefix, wordData, sentenceData) {
  let code = `import type { Word, Sentence } from "@/lib/types"\n\n`
  code += `export const ${lang}Words: Word[] = [\n`
  for (const w of wordData) {
    const tr = w.transliteration ? `, transliteration: "${w.transliteration}"` : ""
    code += `  { id: "${w.id}", source: "${w.source}", target: "${w.target}"${tr}, category: "${w.category}" },\n`
  }
  code += `]\n\n`
  code += `export const ${lang}Sentences: Sentence[] = [\n`
  for (const s of sentenceData) {
    const tr = s.transliteration ? `, transliteration: "${s.transliteration}"` : ""
    code += `  { id: "${s.id}", source: "${s.source}", target: "${s.target}"${tr} },\n`
  }
  code += `]\n`

  const dir = join(process.cwd(), "src", "data")
  const filePath = join(dir, `${lang}.ts`)
  writeFileSync(filePath, code, "utf8")
  console.log(`Wrote ${filePath}`)
}

