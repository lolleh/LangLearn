function buildWords(prefix, data) {
  let id = 1
  const words = []
  for (const [count1, cat, entries] of data) {
    for (const entry of entries) {
      const parts = entry.split("|")
      const word = { id: `${prefix}-${id++}`, source: parts[0], target: parts[1], category: cat }
      if (parts[2]) word.transliteration = parts[2]
      words.push(word)
    }
  }
  return words
}

function buildSentences(prefix, list) {
  return list.map((entry, i) => {
    const parts = entry.split("|")
    const s = { id: `${prefix}-s-${i + 1}`, source: parts[0], target: parts[1] }
    if (parts[2]) s.transliteration = parts[2]
    return s
  })
}

